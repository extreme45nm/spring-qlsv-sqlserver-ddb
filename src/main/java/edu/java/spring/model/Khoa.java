package edu.java.spring.model;

public class Khoa {
	
	private String makhoa;
	private String tenkhoa;
	private String malanhdaokhoa;
	
	public Khoa(){}
	
	public String getMakhoa() {
		return makhoa;
	}
	public void setMakhoa(String makhoa) {
		this.makhoa = makhoa;
	}
	public String getTenkhoa() {
		return tenkhoa;
	}
	public void setTenkhoa(String tenkhoa) {
		this.tenkhoa = tenkhoa;
	}
	public String getMalanhdaokhoa() {
		return malanhdaokhoa;
	}
	public void setMalanhdaokhoa(String lanhdaokhoa) {
		this.malanhdaokhoa = lanhdaokhoa;
	}
}
